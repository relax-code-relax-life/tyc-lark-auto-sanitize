#!/bin/zsh


# ==========================================
# README:
# 1. 需执行 chmod +x run.sh 赋予执行权限

# 2. crontab -e

# # 分 时 日 月 周 命令
# 25 09 * * * /Users/wangwl.net/tyc/lark_auto/run.sh
# 10 19 * * * /Users/wangwl.net/tyc/lark_auto/run.sh

# 3. crontab -l
# ==========================================

# ==========================================
# 1. 进入项目目录
# ==========================================
# ⚠️ 确保这里是你真实的路径
cd /Users/wangwl.net/tyc/lark_auto

# ==========================================
# 2. 记录初始日志
# ==========================================
echo "------------------------------------------------------" >> run.log
echo "⏰ Cron唤醒时间: $(date)" >> run.log

# ==========================================
# 3. 随机延迟逻辑 (0~180秒)
# ==========================================
# 生成 0 到 180 之间的随机数
# 180秒 = 3分钟，覆盖 09:25 到 09:28 这个区间
RANDOM_DELAY=$(($RANDOM % 181))

echo "😴 启用拟人化延迟，随机等待: ${RANDOM_DELAY}秒..." >> run.log

# 脚本暂停执行
sleep $RANDOM_DELAY

echo "⚡️ 延迟结束，开始执行 Python 任务: $(date)" >> run.log

# ==========================================
# 4. 调用打卡脚本
# ==========================================
/Users/wangwl.net/tyc/lark_auto/exec.sh