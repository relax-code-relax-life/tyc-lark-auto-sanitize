#!/bin/zsh

# ==========================================
# README:
# 执行一次打卡逻辑（可单独手动运行）
# 由 run.sh 调用（cron 场景），也可直接执行：
#   chmod +x exec.sh
#   ./exec.sh
# ==========================================

# ==========================================
# 0. 双路输出：同时打印到控制台 & 追加到 run.log
# ==========================================
exec > >(tee -a /Users/wangwl.net/tyc/lark_auto/run.log) 2>&1

# ==========================================
# 1. 加载环境变量
# ==========================================

# 让脚本拥有和终端一样的 Java/ADB 环境
source ~/.zshrc

# 显式指定 PATH (防止 .zshrc 某些情况下未生效)，确保能找到 adb 和 appium
export PATH=$PATH:/opt/homebrew/bin:/usr/local/bin

# ==========================================
# 2. 进入项目目录
# ==========================================
cd /Users/wangwl.net/tyc/lark_auto

# ==========================================
# 3. 执行 Python 脚本
# ==========================================
/opt/miniconda3/envs/lark_auto/bin/python main.py
